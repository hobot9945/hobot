/**
 * glob.js
 *
 * Глобальная конфигурация и управление состоянием (State Management).
 * Этот скрипт загружается первым и предоставляет другим модулям:
 * 1. Константы (селекторы, теги).
 * 2. Глобальный флаг занятости агента (isAgentBusy):
 *   - влияет на FocusGuard (background.js) и FocusManager (focus_manager.js),
 *   - используется как сигнал "идёт критическая операция: не переключать вкладку и не терять фокус ввода".
 * 3. Утилиты логирования.
 */
window.Globals = {
    // --- 1. КОНСТАНТЫ И КОНФИГУРАЦИЯ ---

    // Способ использования расширения. Расширение может работать в двух модах, LOGGER_MODE == true,
    // расширение пересылает агенту сырой перехваченный текст без выделения/проверки директив. На стороне агента должен
    // быть включён log_only = true в config.toml. В этом режиме агент только записывает в журнал принятый от
    // расширения текст. Текст не парсится и не исполняется. Такая комбинация используется для отладки.
    // Если LOGGER_MODE == false, директивы выделяются и отдаются на обработку агенту.
    LOGGER_MODE: false,

    // Флаг отладки. Выдаются отладочные сообщения.
    IS_DEBUG: true,

    // Состояния кнопки управления исполнением команд агента (совпадают с CSS-классами popup). "paused" исполняется
    // расширением. "step"/"auto" пересылаются агенту и исполняются им.
    execMode: Object.freeze({
        PAUSED: "paused",   // исполнение команд агентом остановлено
        STEP: "step",       // пошаговый режим: каждая директива подтверждается пользователем
        AUTO: "auto",       // автоматический режим: исполнение директив без подтверждения
    }),

    // Состояния кнопки уровня доступа команд агента. Пересылаются агенту, исполняются им.
    osWriteMode: Object.freeze({
        READ: "read",       // разрешены безопасные команды (белый список), остальные требуют подтверждения
        WRITE: "write",     // все команды исполняются агентом без подтверждения.
    }),

    // Маркерные скобки для тегов.
    DIRECTIVE_BRACKET: { BRA: "<<<ai", KET: ">>>ai" },   // Директивы AI -> Хобот
    EXTENSION_BRACKET: { BRA: "<<<ext", KET: ">>>ext" }, // Служебные Ext -> Хобот
    AGENT_BRACKET:     { BRA: "<<<hbt", KET: ">>>hbt" }, // Отчеты Хобот -> AI

    // Во что сайт заворачивает блоки кода. Важно для парсинга в text_processor.
    webSiteCodeBlocksWrapping: Object.freeze({
        UNKNOWN: 0,     //
        PRE: 1,
        DIV: 2
    }),

    // --- 2. Глобальные переменные ---

    // Выполнена ли инициализация (полная, приложения и агента). Управляющий флаг, используется в content.js при
    // старте расширения или по кнопке ПАУЗА/РАБОТА.
    isHobotInitialized: false,

    // При парсинге сайта можно рассчитывать на то, что сайт заворачивает блоки кода в pre.
    isItPreBasedSite: false,

    // При парсинге сайта можно рассчитывать на то, что сайт заворачивает блоки кода в это.
    webParsingType: 0,

    // Приватное поле состояния (доступ только через методы)
    _isAgentBusy: false,

    // Уникальный идентификатор сессии генерируется в модуле hobot_bridge.js в момент формирования пакета инициализации
    // для Хобота, посылается ему и сохраняется здесь. Используется DirExtractor для проверки актуальности директив AI.
    sessionId: null,

    // Текущее состояние кнопок управления Хоботом.
    buttonState: null,

    /**
     * Устанавливает глобальный флаг занятости агента.
     * @param {boolean} busy - Новый статус
     */
    setIsAgentBusy(busy) {
        if (this._isAgentBusy === busy) return;

        this._isAgentBusy = busy;

        // Оповещаем background.js (для защиты вкладок)
        // Делаем это асинхронно, не блокируя UI
        if (chrome && chrome.runtime) {
            chrome.runtime.sendMessage({
                type: "UPDATE_BUSY_STATE",
                busy: busy
            }).catch(() => {});
        }

        // Вызов колбэка для FocusManager.
        window.focusManager.onChangeAgentBusyState();
    },

    /**
     * Получить текущий статус.
     * @returns {boolean}
     */
    getIsAgentBusy() {
        return this._isAgentBusy;
    },


    /**
     * Задержка на N мс (аналог Rust thread::sleep).
     * @param {number} ms
     * @returns {Promise<void>}
     */
    delay(ms) {
        return new Promise(resolve => setTimeout(resolve, ms));
    }
};
